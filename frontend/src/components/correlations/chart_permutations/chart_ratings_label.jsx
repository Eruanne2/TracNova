import React from 'react';
export default function CUSTOM_LABEL(e) {
    if (e === 0) {
      return ""
    } else if (e === 1) {
      return "😒"
    } else if (e === 2) {
      return "😕"
    } else if (e === 3) {
      return "🙂"
    } else if (e === 4) {
      return "😊"
    } else {
      return "😁"
    }}